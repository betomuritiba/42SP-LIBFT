#include "libft.h"

char	*ft_strdup(const char *str)
{
	char	*ret;
	int		size;
	int		i;

	size = ft_strlen(str);
	ret = (char *)malloc(sizeof(char) * size + 1);
	if (!ret)
		return (NULL);
	i = 0;
	while (str[i])
	{
		ret[i] = str[i];
		i++;
	}
	ret[i] = '\0';
	return (ret);
}
